package day07;

public class Calc {;
	public static int add(int a, int b){    //add하기 위한 정수값 2개 (매개변수 설정)   //void> 값없이 그냥 return하기. (선언부 이해)
		return a+b; 
	} 
	public static int sub(int a, int b){   
		return a-b;
	}
	public static int multi(int a, int b){   
		return a*b;
	}
	public static int div(int a, int b){   
		return a/b;
	}
}
